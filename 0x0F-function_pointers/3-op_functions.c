/**
 * op_add - add two intgers
 * @a: first number
 * @b: second number
 * Return: the sum
 * Ashraf Atef
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - sub two intgers
 * @a: first number
 * @b: second number
 * Return: the sum
 * Ashraf Atef
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two intgers
 * @a: first number
 * @b: second number
 * Return: the sum
 * Ashraf Atef
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two intgers
 * @a: first number
 * @b: second number
 * Return: the sum
 * Ashraf Atef
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod two intgers
 * @a: first number
 * @b: second number
 * Return: the sum
 * Ashraf Atef
 */
int op_mod(int a, int b)
{
	return (a % b);
}
